class IntroPagerAdapter extends PagerAdapter {

    private final Context context;
    private final int[] slides = {
            R.layout.intro_slide1,
            R.layout.intro_slide2,
            R.layout.intro_slide3,
    };

    public IntroPagerAdapter(Context context) {
        this.context = context;
    }

    @Override
    public int getCount() {
        return slides.length;
    }

    @NonNull
    @Override
    public Object instantiateItem(@NonNull ViewGroup container, int position) {
        LayoutInflater inflater = LayoutInflater.from(context);
        ViewGroup layout = (ViewGroup) inflater.inflate(
                slides[position], container, false);
        container.addView(layout);
        return layout;
    }

    @Override
    public void destroyItem(@NonNull ViewGroup container, int position, @NonNull Object object) {
        container.removeView((View) object);
    }

    @Override
    public boolean isViewFromObject(@NonNull View view, @NonNull Object object) {
        return view == object;
    }
}
